

public class ProdutosGrafica {
    private int id_produto;
    private String nm_nome;
    private String de_descricao;
    private double vl_preco;
    private int qnt_estoque;

    public int getId_produto() {
        return id_produto;
    }

    public void setId_produto(int id_produto) {
        this.id_produto = id_produto;
    }

    public String getNm_nome() {
        return nm_nome;
    }

    public void setNm_nome(String nm_nome) {
        this.nm_nome = nm_nome;
    }

    public String getDe_descricao() {
        return de_descricao;
    }

    public void setDe_descricao(String de_descricao) {
        this.de_descricao = de_descricao;
    }

    public double getVl_preco() {
        return vl_preco;
    }

    public void setVl_preco(double vl_preco) {
        this.vl_preco = vl_preco;
    }

    public int getQnt_estoque() {
        return qnt_estoque;
    }

    public void setQnt_estoque(int qnt_estoque) {
        this.qnt_estoque = qnt_estoque;
    }
}

